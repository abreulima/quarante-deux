void draw_tile(program *vars, int x, int y, char type, Camera cam)
{
    if (type == '1')
        mlx_put_image_to_window(vars->mlx, vars->window, vars->tiles[0], -cam.x + 32 * x, -cam.y + 32 * y);
}

void draw_map(program *vars)
{
    int row;
    int col;

    row = 0;
    while (row < ROWS)
    {
        col = 0;
        while (col < COLS)
        {
            if (map[row][col] == '1')
                draw_tile(vars, col, row, '1', vars->cam);
            col++;
        }
        row++;
    }

}